import java.util.*;
public class Averageof3 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        float A = sc.nextFloat();
        float B = sc.nextFloat();
        float C = sc.nextFloat();

        float average = (A+B+C)/3;
        System.out.println(average);
    }
}
